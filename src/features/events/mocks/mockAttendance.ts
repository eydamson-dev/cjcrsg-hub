export const getAttendanceStats = (records: AttendanceRecord[]) => {
  const members = records.filter((r) => r.attendee?.status === 'member').length
  const visitors = records.filter(
    (r) => r.attendee?.status === 'visitor',
  ).length
  return {
    total: records.length,
    members,
    visitors,
  }
}
